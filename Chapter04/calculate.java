package Chapter04;

public class calculate {

    static public int add(int a,int b){
        return a+b;
    }

    static public int sub(int a,int b){
        return a-b;
    }

    static public int mul(int a,int b){
        return a*b;
    }

    static public int div(int a,int b){
        return a/b;
    }

}
